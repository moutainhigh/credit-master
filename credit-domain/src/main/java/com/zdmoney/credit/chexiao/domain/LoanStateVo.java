package com.zdmoney.credit.chexiao.domain;

public class LoanStateVo {
		private String loanid;
		private String updateLoanState;
		
		public LoanStateVo() {
			super();
		}

		public String getLoanid() {
			return loanid;
		}

		public void setLoanid(String loanid) {
			this.loanid = loanid;
		}

		public String getUpdateLoanState() {
			return updateLoanState;
		}
		public void setUpdateLoanState(String updateLoanState) {
			this.updateLoanState = updateLoanState;
		}
	
		
}
